// ── HELPERS ──────────────────────────────────────────────────────────────────

export function svcPrice(id, lang) {
  const svc = SERVICES.find(s => s.id === id);
  if (!svc) return 0;
  return lang === 'sr' ? svc.eurPrice * 100 : svc.eurPrice;
}

export function totalPrice(ids, lang) {
  return ids.reduce((s, id) => s + svcPrice(id, lang), 0);
}

export function totalDuration(ids) {
  return ids.reduce((s, id) => {
    const svc = SERVICES.find(x => x.id === id);
    return s + (svc ? svc.duration : 0);
  }, 0);
}

export function getServiceName(id, t) {
  for (const cat of t.serviceCategories) {
    const found = cat.services.find(s => s.id === id);
    if (found) return found.name;
  }
  return '';
}

export function getServiceNameWithCategory(id, t) {
  for (const cat of t.serviceCategories) {
    const found = cat.services.find(s => s.id === id);
    if (found) return `${found.name} (${cat.label})`;
  }
  return '';
}

export function fmtPrice(n, lang) {
  if (lang === 'sr') return n.toLocaleString('sr-RS') + ' RSD';
  return '\u20ac' + n;
}

export function fmtDur(min) {
  return min >= 60 ? `${Math.floor(min / 60)}h${min % 60 ? ` ${min % 60} min` : ''}` : `${min} min`;
}

export function dateLabel(dateStr, t) {
  if (!dateStr) return '';
  const todayStr = new Date().toISOString().slice(0, 10);
  const tomorrowDate = new Date(); tomorrowDate.setDate(tomorrowDate.getDate() + 1);
  const tomorrowStr = tomorrowDate.toISOString().slice(0, 10);
  if (dateStr === todayStr) return t.today;
  if (dateStr === tomorrowStr) return t.tomorrow;
  const d = new Date(dateStr + 'T12:00:00');
  const dow = t.days[(d.getDay() + 6) % 7];
  return `${dow}, ${d.getDate()} ${t.months[d.getMonth()]}`;
}

export function confirmDateLabel(dateStr, time, t) {
  if (!dateStr || !time) return '';
  const d = new Date(dateStr + 'T12:00:00');
  return `${d.getDate()} ${t.months[d.getMonth()]}, ${time}`;
}
